import fs from "node:fs";
import path from "node:path";
import dgram from "node:dgram";
import { parseArgs } from "./cli-utils.mjs";
import {
  GENERATED_DIR,
  KUBEASZ_DIR,
  ROOT_DIR,
  ensureRuntimeDirs,
  localArch,
  normalizeArch,
  nodesForArch,
  readClusterData,
} from "./cluster-utils.mjs";

const { config, nodes } = readClusterData();
ensureRuntimeDirs();

const rawArgs = parseArgs(process.argv);
const args = {
  mode: rawArgs.mode ?? "full",
  out: rawArgs.out ?? null,
  targetArch: rawArgs["target-arch"] ?? null,
  includeNodes: rawArgs["include-nodes"]
    ? rawArgs["include-nodes"]
        .split(",")
        .map((item) => item.trim())
        .filter(Boolean)
    : null,
};

function shellQuote(value) {
  return `'${String(value).replaceAll("'", `'\"'\"'`)}'`;
}

function renderHostLine(node) {
  return [
    node.ip,
    `ansible_host=${node.ip}`,
    `ip=${node.ip}`,
    `access_ip=${node.ip}`,
    `ansible_user=${shellQuote(node.sshUser)}`,
    `ansible_ssh_pass=${shellQuote(node.sshPassword)}`,
    `ansible_ssh_port=${node.sshPort}`,
    `ansible_become=true`,
    `ansible_become_method=sudo`,
    `ansible_become_user=root`,
    `ansible_become_pass=${shellQuote(node.sshPassword)}`,
    `ansible_python_interpreter=/usr/bin/python3`,
    `k8s_nodename=${node.name}`,
  ].join(" ");
}

function renderGroup(title, items) {
  return [`[${title}]`, ...items, ""].join("\n");
}

function renderVarsGroup(title, vars) {
  return [
    `[${title}]`,
    ...Object.entries(vars).map(([key, value]) => `${key}=${value}`),
    "",
  ].join("\n");
}

function iniDoubleQuote(value) {
  return `"${String(value).replaceAll('"', '\\"')}"`;
}

async function detectControllerIp(targetIp) {
  if (
    typeof config.controllerIp === "string" &&
    config.controllerIp.length > 0
  ) {
    return config.controllerIp;
  }

  return await new Promise((resolve, reject) => {
    const socket = dgram.createSocket("udp4");

    socket.once("error", (error) => {
      socket.close();
      reject(error);
    });

    socket.connect(22, targetIp, () => {
      const address = socket.address();
      socket.close();
      if (!address || typeof address !== "object" || !("address" in address)) {
        reject(new Error("无法自动探测 controllerIp"));
        return;
      }
      resolve(address.address);
    });
  });
}

function resolveChronyHosts() {
  if (config.enableChrony !== true) {
    return [];
  }

  if (
    typeof config.chronyServerNode === "string" &&
    config.chronyServerNode.length > 0
  ) {
    const matched = nodes.find(
      (node) =>
        node.name === config.chronyServerNode ||
        node.ip === config.chronyServerNode,
    );

    if (!matched) {
      throw new Error(
        `cluster/config.json 中的 chronyServerNode 未匹配到节点: ${config.chronyServerNode}`,
      );
    }

    return [matched.ip];
  }

  return [nodes[0].ip];
}

function effectiveProxyMode() {
  if (typeof config.proxyMode === "string" && config.proxyMode.length > 0) {
    return config.proxyMode;
  }

  const configuredArchs = new Set(
    nodes.map((node) => normalizeArch(node.arch)),
  );
  return configuredArchs.size > 1 ? "iptables" : "ipvs";
}

const effectiveArch = args.targetArch ?? localArch();
const proxyMode = effectiveProxyMode();
const targetNodes = args.includeNodes
  ? args.includeNodes.map((name) => {
      const matched = nodes.find((node) => node.name === name);
      if (!matched) {
        throw new Error(`--include-nodes 指定了不存在的节点: ${name}`);
      }
      return matched;
    })
  : args.mode === "bootstrap"
    ? nodesForArch(nodes, effectiveArch)
    : nodes;

if (targetNodes.length === 0) {
  throw new Error(
    `当前模式 ${args.mode} 下没有可用节点。请检查 cluster/nodes.json 中的 arch 配置。`,
  );
}

if (
  args.mode === "bootstrap" &&
  (!targetNodes.some((node) => node.roles.includes("master")) ||
    !targetNodes.some((node) => node.roles.includes("etcd")) ||
    !targetNodes.some((node) => node.roles.includes("worker")))
) {
  throw new Error(
    `bootstrap 模式下缺少同架构的 master/etcd/worker 角色，请调整 cluster/nodes.json。`,
  );
}

const skippedNodes = nodes.filter((node) => !targetNodes.includes(node));
const controllerIp = await detectControllerIp(targetNodes[0].ip);
const chronyHosts =
  args.mode === "bootstrap"
    ? resolveChronyHosts().filter((ip) =>
        targetNodes.some((node) => node.ip === ip),
      )
    : resolveChronyHosts();

const hostsLines = [
  "# Generated by infra/k8s/bin/render-cluster.mjs",
  "# Edit cluster/config.json and cluster/nodes.json instead of this file.",
  `# Mode: ${args.mode}`,
  "",
  renderGroup(
    "all",
    targetNodes.map((node) => renderHostLine(node)),
  ),
  renderGroup(
    "etcd",
    targetNodes
      .filter((node) => node.roles.includes("etcd"))
      .map((node) => node.ip),
  ),
  renderGroup(
    "kube_master",
    targetNodes
      .filter((node) => node.roles.includes("master"))
      .map((node) => node.ip),
  ),
  renderGroup(
    "kube_node",
    targetNodes
      .filter((node) => node.roles.includes("worker"))
      .map((node) => node.ip),
  ),
  renderGroup("harbor", []),
  renderGroup("ex_lb", []),
  renderGroup("chrony", chronyHosts),
  renderVarsGroup("all:vars", {
    SECURE_PORT: iniDoubleQuote("6443"),
    CONTAINER_RUNTIME: iniDoubleQuote("containerd"),
    CLUSTER_NETWORK: iniDoubleQuote("calico"),
    PROXY_MODE: iniDoubleQuote(proxyMode),
    SERVICE_CIDR: iniDoubleQuote("10.68.0.0/16"),
    CLUSTER_CIDR: iniDoubleQuote("172.20.0.0/16"),
    NODE_PORT_RANGE: iniDoubleQuote("30000-32767"),
    CLUSTER_DNS_DOMAIN: iniDoubleQuote("cluster.local"),
    bin_dir: iniDoubleQuote("/opt/kube/bin"),
    base_dir: iniDoubleQuote("/etc/kubeasz"),
    cluster_dir: iniDoubleQuote(
      `{{ base_dir }}/clusters/${config.clusterName}`,
    ),
    ca_dir: iniDoubleQuote("/etc/kubernetes/ssl"),
    k8s_nodename: iniDoubleQuote(""),
    local_registry_host: iniDoubleQuote(controllerIp),
    ansible_python_interpreter: "/usr/bin/python3",
  }),
].join("\n");

const summary = {
  clusterName: config.clusterName,
  kubeaszVersion: config.kubeaszVersion,
  kubernetesVersion: config.kubernetesVersion,
  kubeaszRepoUrl: config.kubeaszRepoUrl,
  workspaceNamespace: config.workspaceNamespace,
  controllerIp,
  effectiveArch,
  enableChrony: config.enableChrony === true,
  mode: args.mode,
  proxyMode,
  includedNodes: targetNodes,
  skippedNodes,
  kubeaszDir: KUBEASZ_DIR,
  rootDir: ROOT_DIR,
};

const outputPath =
  args.out ??
  path.join(
    GENERATED_DIR,
    args.mode === "full" ? "hosts" : `hosts-${args.mode}`,
  );

fs.writeFileSync(outputPath, `${hostsLines}\n`, "utf8");
fs.writeFileSync(
  path.join(
    GENERATED_DIR,
    args.mode === "full"
      ? "cluster-summary.json"
      : `cluster-summary-${args.mode}.json`,
  ),
  `${JSON.stringify(summary, null, 2)}\n`,
  "utf8",
);

console.log(`Rendered kubeasz hosts to ${outputPath}`);
