import fs from "node:fs";
import path from "node:path";
import {
  GENERATED_DIR,
  KUBEASZ_DIR,
  ROOT_DIR,
  ensureRuntimeDirs,
  readClusterData,
} from "./cluster-utils.mjs";

const { config, nodes } = readClusterData();
ensureRuntimeDirs();

function shellQuote(value) {
  return `'${String(value).replaceAll("'", `'\"'\"'`)}'`;
}

function renderHostLine(node) {
  return [
    node.ip,
    `ansible_host=${node.ip}`,
    `ip=${node.ip}`,
    `access_ip=${node.ip}`,
    `ansible_user=${shellQuote(node.sshUser)}`,
    `ansible_ssh_pass=${shellQuote(node.sshPassword)}`,
    `ansible_ssh_port=${node.sshPort}`,
    `ansible_become=true`,
    `ansible_become_method=sudo`,
    `ansible_become_user=root`,
    `ansible_become_pass=${shellQuote(node.sshPassword)}`,
    `ansible_python_interpreter=/usr/bin/python3`,
    `k8s_nodename=${node.name}`,
  ].join(" ");
}

function renderGroup(title, items) {
  return [`[${title}]`, ...items, ""].join("\n");
}

function renderVarsGroup(title, vars) {
  return [
    `[${title}]`,
    ...Object.entries(vars).map(([key, value]) => `${key}=${value}`),
    "",
  ].join("\n");
}

const hostsLines = [
  "# Generated by infra/remote-work/bin/render-cluster.mjs",
  "# Edit cluster/config.json and cluster/nodes.json instead of this file.",
  "",
  renderGroup(
    "all",
    nodes.map((node) => renderHostLine(node)),
  ),
  renderGroup(
    "etcd",
    nodes.filter((node) => node.roles.includes("etcd")).map((node) => node.ip),
  ),
  renderGroup(
    "kube_master",
    nodes.filter((node) => node.roles.includes("master")).map((node) => node.ip),
  ),
  renderGroup(
    "kube_node",
    nodes.filter((node) => node.roles.includes("worker")).map((node) => node.ip),
  ),
  renderGroup("harbor", []),
  renderGroup("ex_lb", []),
  renderGroup(
    "chrony",
    [nodes[0].ip],
  ),
  renderVarsGroup("all:vars", {
    SECURE_PORT: shellQuote("6443"),
    CONTAINER_RUNTIME: shellQuote("containerd"),
    CLUSTER_NETWORK: shellQuote("calico"),
    PROXY_MODE: shellQuote("ipvs"),
    SERVICE_CIDR: shellQuote("10.68.0.0/16"),
    CLUSTER_CIDR: shellQuote("172.20.0.0/16"),
    NODE_PORT_RANGE: shellQuote("30000-32767"),
    CLUSTER_DNS_DOMAIN: shellQuote("cluster.local"),
    bin_dir: shellQuote("/opt/kube/bin"),
    base_dir: shellQuote("/etc/kubeasz"),
    cluster_dir: shellQuote(`{{ base_dir }}/clusters/${config.clusterName}`),
    ca_dir: shellQuote("/etc/kubernetes/ssl"),
    k8s_nodename: shellQuote(""),
    ansible_python_interpreter: "/usr/bin/python3",
  }),
].join("\n");

const summary = {
  clusterName: config.clusterName,
  kubeaszVersion: config.kubeaszVersion,
  kubernetesVersion: config.kubernetesVersion,
  kubeaszRepoUrl: config.kubeaszRepoUrl,
  workspaceNamespace: config.workspaceNamespace,
  kubeaszDir: KUBEASZ_DIR,
  rootDir: ROOT_DIR,
  nodes,
};

fs.writeFileSync(path.join(GENERATED_DIR, "hosts"), `${hostsLines}\n`, "utf8");
fs.writeFileSync(
  path.join(GENERATED_DIR, "cluster-summary.json"),
  `${JSON.stringify(summary, null, 2)}\n`,
  "utf8",
);

console.log(`Rendered kubeasz hosts to ${path.join(GENERATED_DIR, "hosts")}`);
